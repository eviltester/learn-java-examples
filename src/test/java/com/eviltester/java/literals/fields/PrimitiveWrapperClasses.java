package com.eviltester.java.literals.fields;

public class PrimitiveWrapperClasses {

    Integer anInteger = 100; // new Integer(100);
    Boolean aBoolean = true; // new Boolean(true);
    Character aChar =  'a'; //  new Character('a');
    Long aLong = 1L;     // you need the L
    Float aFloat = 1.0f; // you need the F
    Double aDouble = 1.0d; // by default 1.0 is double (the d is optional)
    String aString = "abc";

    String aNullString = null; // primitive types can not be null

}
